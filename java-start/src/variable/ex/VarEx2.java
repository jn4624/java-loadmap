package variable.ex;

/**
 * - 변수 num1 선언, 10 초기화
 * - 변수 num2 선언, 20 초기화
 * - 두 변수의 합 계산, 그 결과를 변수 sum 에 저장
 * - sum 변수 값 출력
 */
public class VarEx2 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int sum = num1 + num2;

        System.out.println(sum);
    }
}
